void register_init(U08 *dat)
{
    U08 idx = 0, val    = 0;
    U16 cnt = 0, offset = 0;

    while ((dat[cnt] != 0xFF) || (dat[cnt+1] != 0xFF))
    {
        if(dat[cnt] == 0xFF)
        {
            offset = dat[cnt+1] << 8;
        }
        else
        {
            idx = dat[cnt];
            val = dat[cnt+1];

            WriteTW88(idx + offset, val);
        }

        cnt += 2;
    }    
}

void tw8836_init(void)
{
    if (ReadTW88(0x000) == 0x36)
        register_init(init_regs);
}
